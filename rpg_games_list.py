def generate_game_urls():
    """Genera lista completa de URLs de juegos"""
    all_games = []

    for platform, data in RPG_LIST.items():
        prefix = data["url_prefix"]
        for game_slug in data["games"]:
            # Construir URL
            game_url = f"{prefix}{game_slug.replace(' ', '').lower()}/"

            # Crear nombre legible
            readable_name = game_slug.replace('-', ' ').replace('_', ' ').title()

            all_games.append({
                "platform": platform,
                "name": readable_name,
                "url": game_url,
                "slug": game_slug.lower().replace(' ', '')
            })

    return all_games

