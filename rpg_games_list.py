    games = generate_game_urls()
    print(f"Total de juegos en lista: {len(games)}\n")

    for i, game in enumerate(games[:50], 1):
        print(f"{i:3}. [{game['platform']:20}] {game['name']}")
        print(f"     {game['url']}")